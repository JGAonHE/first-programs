#convert 'H' to its number value
hNumValue = ord('H')

#translate the number value into binary
hBinValue = bin(hNumValue)[2:]

#convert 'e' to its number value
eNumValue = ord('e')

#translate the  number value into binary
eBinValue = bin(eNumValue)[2:]

#print the binary translation
print(hBinValue, eBinValue)

